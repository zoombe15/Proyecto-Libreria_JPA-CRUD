    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
       String nombre=txtLibro.getText();
       String categoria=(String)cbxCategoria.getSelectedItem();
       double precio=Double.parseDouble(txtPrecio.getText());
       String miembro=(String)cbxMiembro.getSelectedItem();
       
       String nombre_cliente=txtNombre.getText();
       String dni=txtDni.getText();
       String dirección=txtDireccion.getText();
       
       control.guardar(nombre,categoria,precio,miembro,nombre_cliente,dni,dirección);
       
       JOptionPane optionPane =new JOptionPane("Se guardo correctamente");
       optionPane.setMessageType(JOptionPane.INFORMATION_MESSAGE);
       JDialog dialog= optionPane.createDialog("Guardar informacion a BASE DE DATOS");
       dialog.setAlwaysOnTop(true);
       dialog.setVisible(true);
        
    }//GEN-LAST:event_btnGuardarActionPerformed

